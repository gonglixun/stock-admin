package com.hiveview.entity.bluray;

public class Cast  {
	
	//columns START
	private Integer castId;
	private String castName;
	private Integer castType;
	private String castDesc;
	private String castPicture;
	private String castPyname;
	//columns END
	public Integer getCastId() {
		return castId;
	}
	public void setCastId(Integer castId) {
		this.castId = castId;
	}
	public String getCastName() {
		return castName;
	}
	public void setCastName(String castName) {
		this.castName = castName;
	}
	public Integer getCastType() {
		return castType;
	}
	public void setCastType(Integer castType) {
		this.castType = castType;
	}
	public String getCastDesc() {
		return castDesc;
	}
	public void setCastDesc(String castDesc) {
		this.castDesc = castDesc;
	}
	public String getCastPicture() {
		return castPicture;
	}
	public void setCastPicture(String castPicture) {
		this.castPicture = castPicture;
	}
	public String getCastPyname() {
		return castPyname;
	}
	public void setCastPyname(String castPyname) {
		this.castPyname = castPyname;
	}
	
}

